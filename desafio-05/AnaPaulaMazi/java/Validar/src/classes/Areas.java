package classes;

public class Areas {
	private String codigo;
	private String nome;
	

	
	public Areas(String codigo, String nome) {
		this.codigo = codigo;
		this.nome = nome;
		
	}

	public String getCodigo() {
		return codigo;
	}
	
	public void SetCodigo(String codigo) {
		this.codigo = codigo;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
	

}
